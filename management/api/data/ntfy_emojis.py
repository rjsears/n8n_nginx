# Flatten for quick access
def get_all_emojis():
    """Get all emojis as a flat list with category info."""
    result = []
    for category, emojis in EMOJI_CATEGORIES.items():
        for e in emojis:
            result.append({
                "shortcode": e["s"],
                "emoji": e["e"],
                "category": category
            })
    return result

# Get category list
def get_categories():
    """Get list of available categories."""
    return list(EMOJI_CATEGORIES.keys())

# Search emojis
def search_emojis(query: str, limit: int = 50):
    """Search emojis by shortcode."""
    query = query.lower()
    results = []
    for category, emojis in EMOJI_CATEGORIES.items():
        for e in emojis:
            if query in e["s"].lower():
                results.append({
                    "shortcode": e["s"],
                    "emoji": e["e"],
                    "category": category
                })
                if len(results) >= limit:
                    return results
    return results
